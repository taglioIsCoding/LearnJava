package tris.persistence;

public class SadPrinterException extends Exception
{
	private static final long serialVersionUID = 1L;

	public SadPrinterException()
	{
	}

	public SadPrinterException(String arg0)
	{
		super(arg0);
	}

	public SadPrinterException(Throwable arg0)
	{
		super(arg0);
	}

	public SadPrinterException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
